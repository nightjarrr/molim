# ----------------------------------------------------------------------
# Final steps:
# - define exit trap to report git status trail
# - exec CMD (claude by default) with args passed from the launcher
# - drop into interactive bash when CMD exits
# ----------------------------------------------------------------------

git_trail() {
    section "Git state on exit"
    git -C /workspace status || true
    if git -C /workspace rev-parse @{u} >/dev/null 2>&1; then
        git --no-pager -C /workspace log --oneline @{u}..HEAD
    else
        echo "(branch not yet pushed to remote)"
    fi
    read -r -s -n 1 -p $'\nPress any key to exit the container...' _
}

trap git_trail EXIT

# Run CMD (claude by default, or any override). Bootstrap stays strict, but
# command failures after bootstrap should leave the user in an interactive
# shell for inspection instead of terminating the container immediately.
cmd_status=0
"$@" || cmd_status=$?

if [[ "$cmd_status" -ne 0 ]]; then
    warning "Command exited with status ${cmd_status}; dropping into interactive shell."
else
    echo "Command exited successfully; dropping into interactive shell."
fi

bash
exit "$cmd_status"


